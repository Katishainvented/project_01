month = int(input('Введите номер месяца: '))
def quarter_of(month):
    mon = {1 :"1 (январь)", 2 :"2 (февраль)", 3 :"3 (март)", 4 :"4 (апрель)", 5 :"5 (май)", 6 :"6 (июнь)", 7 :"7 (июль)", 8 :"8 (август)", 9 :"9 (сентябрь)", 10 :"10 (октябрь)", 11 :"11 (ноябрь)", 12 :"12(декабрь)"}
    return mon.get(month)
print('Месяц ', quarter_of(month), " является частью ", month//4+1, ' квартала')
